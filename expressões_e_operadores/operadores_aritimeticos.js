// Operadores Aritiméticos

// multiplicação *
console.log(3 * 5);
// divisão /
console.log(12 / 2);
// soma +
console.log(34 + 67);
// subtração -
console.log(34 - 23);
// resto da divisão %
let remainder;
remainder = 11 % 3;
console.log(remainder);
// incremento ++
let increment = 0;
increment++;
console.log(increment);
// decremento --
let decremet = 0;
console.log(decremet--);
// exponencial **
console.log(3 ** 3);
